def PrintOriginalString():
    """ Prints The Original String"""
    nameOfClient = "Gerry Byrne";
    print(nameOfClient);

def PrintUpperCaseString():
    """ Prints The Original String"""
    nameOfClient = "Gerry Byrne";
    print(nameOfClient.upper());

def PrintLowerCaseString():
    """ Prints The Original String"""
    nameOfClient = "Gerry Byrne";
    print(nameOfClient.lower());

# Display the original string by calling the PrintOriginalString() function
PrintOriginalString();

# Display the string as upper case by calling the PrintUpperCaseString() function
PrintUpperCaseString();

# Display the string as lower case by calling the PrintLowerCaseString() function
PrintLowerCaseString();